# Запит користувача на введення ціни одного олівця
ціна_олівця = float(input("Введіть ціну одного олівця в гривнях: "))

# Запит користувача на введення кількості олівців
кількість_олівців = int(input("Введіть кількість олівців: "))

# Обчислення вартості всіх олівців
вартість_в_гривнях = ціна_олівця * кількість_олівців

# Виведення вартості всіх олівців
print("Вартість", кількість_олівців, "олівців:", вартість_в_гривнях, "гривень")
